def parse_cfg(path):
    assert os.path.exists(path), "%s not found!" % path

    with open(path, "r") as f:
        lines = f.readlines()

    lines = [x.strip() for x in lines if x.strip() and not x.startswith("#")]
    modules = []
    for line in lines:
        if line.startswith("["):
            modules.append(dict())
            modules[-1]['type'] = line[1:-1].strip()
            if modules[-1]['type'] == 'convolutional':
                modules[-1]['batch_normalize'] = 0  # pre-populate with zeros (may be overwritten later)
        else:
            key, val = line.split("=")
            key = key.strip()
            val = val.strip()

            if key == 'anchors':
                modules[-1][key] = np.array([float(x) for x in val.split(',')]).reshape((-1, 2))
            elif (key in ['from', 'layers', 'mask']) or (key == 'size' and ',' in val):
                modules[-1][key] = [int(x) for x in val.split(',')]
            else:
                val = val.strip()
                if val.isnumeric():  # return int or float
                    modules[-1][key] = int(val)
                elif isfloat(val):
                    modules[-1][key] = float(val)
                else:
                    modules[-1][key] = val  # return string

    # Check all fields are supported
    supported = ['type', 'batch_normalize', 'filters', 'size', 'stride', 'pad', 'activation', 'layers', 'groups',
                 'from', 'mask', 'anchors', 'classes', 'num', 'jitter', 'ignore_thresh', 'truth_thresh', 'random',
                 'stride_x', 'stride_y', 'weights_type', 'weights_normalization', 'scale_x_y', 'beta_nms', 'nms_kind',
                 'iou_loss', 'iou_normalizer', 'cls_normalizer', 'iou_thresh']

    f = []  # fields
    for x in modules[1:]:
        [f.append(k) for k in x if k not in f]
    u = [x for x in f if x not in supported]  # unsupported fields
    if any(u):
        print("WARNING: Unsupported fields: %s in %s" % (u, path))

    return modules

